SET NAMES utf8mb4;
CREATE DATABASE IF NOT EXISTS power_demo CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci;
USE power_demo;

CREATE TABLE IF NOT EXISTS dim_station_area (
  station_area_id BIGINT PRIMARY KEY,
  station_area_name VARCHAR(128) NOT NULL,
  power_station_name VARCHAR(128) NOT NULL,
  line_name VARCHAR(128) NOT NULL,
  region_name VARCHAR(64) NOT NULL
);

CREATE TABLE IF NOT EXISTS energy_daily (
  id BIGINT PRIMARY KEY AUTO_INCREMENT,
  stat_date DATE NOT NULL,
  station_area_id BIGINT NOT NULL,
  energy_kwh DECIMAL(18,2) NOT NULL
);

CREATE TABLE IF NOT EXISTS load_daily (
  id BIGINT PRIMARY KEY AUTO_INCREMENT,
  stat_date DATE NOT NULL,
  station_area_id BIGINT NOT NULL,
  max_load_kw DECIMAL(18,2) NOT NULL,
  avg_load_kw DECIMAL(18,2) NOT NULL
);

CREATE TABLE IF NOT EXISTS line_loss_daily (
  id BIGINT PRIMARY KEY AUTO_INCREMENT,
  stat_date DATE NOT NULL,
  station_area_id BIGINT NOT NULL,
  line_loss_rate DECIMAL(10,4) NOT NULL
);

